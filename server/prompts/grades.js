/** 前端年级 key → V2 Prompt key */
const GRADE_KEY_MAP = {
  '1-2': 'lower',
  '3-4': 'middle',
  '5-6': 'upper',
};

export const DEFAULT_GRADE = '3-4';

export function getGradePrompt(gradeKey) {
  const promptKey = GRADE_KEY_MAP[gradeKey] || GRADE_KEY_MAP[DEFAULT_GRADE];
  return gradePrompts[promptKey];
}
